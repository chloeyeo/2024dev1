package javaSample;

public class SamsungTV{
	public SamsungTV() {
		System.out.println("created a new Samsung TV");
	}
	public void powerOn() {
		System.out.println("turned on the Samsung TV");
	}
	public void powerOff() {
		System.out.println("turned off the Samsung TV");
	}
	public void volumeOn() {
		System.out.println("turned on the volume of the Samsung TV");
	}
	public void volumeOff() {
		System.out.println("turned off the volume of the Samsung TV");
	}
}
